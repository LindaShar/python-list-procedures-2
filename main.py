# initializing a list
my_list = [5, 2, 1, 4, 5, 2, 3, 4, 5, 1]

# removing duplicates
my_list = list(set(my_list))

# reversing the order
my_list = my_list[::-1]

# arranging list by changing pointers
my_list.sort()

# arranging list by changing item values
my_list = sorted(my_list)

print(my_list)
